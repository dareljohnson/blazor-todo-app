/// </summary>
public enum TodoPriority
{
    /// <summary>
    /// Low priority task.
    /// </summary>
    Low = 0,

    /// <summary>
    /// Medium priority task.
    /// </summary>
    Medium = 1,

    /// <summary>
    /// High priority task.
    /// </summary>
    High = 2
}
